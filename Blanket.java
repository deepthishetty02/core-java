class Blanket {
    public static void main(String[] args) {

        int size;
        System.out.println("I am printing declaration part of size");
        size = 5;
        System.out.println("I am printing initialization part of size " + size);

        float weight;
        System.out.println("I am printing declaration part of weight");
        weight = 2.5f;
        System.out.println("I am printing initialization part of weight " + weight);

        double price;
        System.out.println("I am printing declaration part of price");
        price = 899.99;
        System.out.println("I am printing initialization part of price " + price);

        char grade;
        System.out.println("I am printing declaration part of grade");
        grade = 'A';
        System.out.println("I am printing initialization part of grade " + grade);

        boolean warm;
        System.out.println("I am printing declaration part of warm");
        warm = true;
        System.out.println("I am printing initialization part of warm " + warm);

        String color;
        System.out.println("I am printing declaration part of color");
        color = "Blue";
        System.out.println("I am printing initialization part of color " + color);

    }
}