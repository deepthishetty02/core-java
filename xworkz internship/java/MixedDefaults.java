class MixedDefaults {

    static boolean flag;
    static double dValue;
    static byte bValue;
    static String text;
    static int number;
    static char letter;
    static long longNumber;
    static float fValue;
    static short sValue;

    public static void main(String[] args) {

        System.out.println("Boolean Default: " + flag);
        System.out.println("Double Default: " + dValue);
        System.out.println("Byte Default: " + bValue);
        System.out.println("String Default: " + text);
        System.out.println("Int Default: " + number);
        System.out.println("Char Default: " + letter);
        System.out.println("Long Default: " + longNumber);
        System.out.println("Float Default: " + fValue);
        System.out.println("Short Default: " + sValue);
    }
}