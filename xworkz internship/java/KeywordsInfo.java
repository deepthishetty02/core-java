class KeywordsInfo {
    public static void main(String[] args) {

        System.out.println("byte : stores tiny numbers");
        System.out.println("short : stores small numbers");
        System.out.println("int : stores whole numbers");
        System.out.println("long : stores big numbers");
        System.out.println("float : stores decimal values");
        System.out.println("double : stores precise decimals");
        System.out.println("char : stores single letter");
        System.out.println("boolean : stores true/false");

        System.out.println("class : creates objects");
        System.out.println("interface : defines methods");
        System.out.println("extends : inherits class");
        System.out.println("implements : uses interface");
        System.out.println("package : groups classes");
        System.out.println("import : uses package");

        System.out.println("public : access anywhere");
        System.out.println("private : access inside class");
        System.out.println("protected : access within package");

        System.out.println("if : decision making");
        System.out.println("else : alternative decision");
        System.out.println("switch : multiple decisions");
        System.out.println("case : specific option");
        System.out.println("default : last option");
        System.out.println("for : loop with limit");
        System.out.println("while : loop with condition");
        System.out.println("do : runs once first");
        System.out.println("break : stops loop");
        System.out.println("continue : skips step");
        System.out.println("return : sends result");

        System.out.println("try : risky code");
        System.out.println("catch : handles error");
        System.out.println("finally : always runs");
        System.out.println("throw : creates error");
        System.out.println("throws : declares error");

        System.out.println("new : creates object");
        System.out.println("this : current object");
        System.out.println("super : parent object");
        System.out.println("instanceof : checks type");

        System.out.println("static : common for all");
        System.out.println("final : cannot change");
        System.out.println("void : no value");
        System.out.println("abstract : incomplete design");
        System.out.println("synchronized : controls threads");
        System.out.println("volatile : updates instantly");
        System.out.println("transient : not saved");
        System.out.println("native : external code");
        System.out.println("strictfp : exact decimals");
        System.out.println("assert : testing check");
        System.out.println("enum : fixed values");

        System.out.println("const : reserved keyword");
        System.out.println("goto : reserved keyword");
    }
}