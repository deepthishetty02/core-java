class Bucket {
    public static void main(String[] args) {

        String material;
        System.out.println("Declaring variable material");
        material = "Plastic";
        System.out.println("Value stored in material is " + material);

        int capacity;
        System.out.println("Declaring variable capacity");
        capacity = 20;
        System.out.println("Value stored in capacity is " + capacity);

        float price;
        System.out.println("Declaring variable price");
        price = 150.75f;
        System.out.println("Value stored in price is " + price);

        double weight;
        System.out.println("Declaring variable weight");
        weight = 1.25;
        System.out.println("Value stored in weight is " + weight);

        char sizeCode;
        System.out.println("Declaring variable sizeCode");
        sizeCode = 'M';
        System.out.println("Value stored in sizeCode is " + sizeCode);

        boolean handleAvailable;
        System.out.println("Declaring variable handleAvailable");
        handleAvailable = true;
        System.out.println("Value stored in handleAvailable is " + handleAvailable);

    }
}