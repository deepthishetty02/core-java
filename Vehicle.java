class Vehicle 
{
    public static void main(String[] args) {

        String vehicleName = "Honda Activa";
        System.out.println("Vehicle Name: " + vehicleName);

        int wheels = 2;
        System.out.println("Number of Wheels: " + wheels);

        boolean electric = false;
        System.out.println("Electric Vehicle: " + electric);

        double price = 85000.50;
        System.out.println("Price: " + price);

        char fuelType = 'P';
        System.out.println("Fuel Type: " + fuelType);

        byte gears = 4;
        System.out.println("Gears: " + gears);

        float mileage = 45.5f;
        System.out.println("Mileage: " + mileage);

        long registrationNumber = 123456789L;
        System.out.println("Registration Number: " + registrationNumber);

        short serviceInterval = 6;
        System.out.println("Service Interval (months): " + serviceInterval);
    }
}