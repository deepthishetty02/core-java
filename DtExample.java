class DtExample {

    static long lValue;
    static char ch;
    static float fNum;
    static short sNum;
    static String message;
    static boolean status;
    static int count;
    static double dNum;
    static byte bNum;

    public static void main(String[] args) {

        System.out.println("Long Default: " + lValue);
        System.out.println("Char Default: " + ch);
        System.out.println("Float Default: " + fNum);
        System.out.println("Short Default: " + sNum);
        System.out.println("String Default: " + message);
        System.out.println("Boolean Default: " + status);
        System.out.println("Int Default: " + count);
        System.out.println("Double Default: " + dNum);
        System.out.println("Byte Default: " + bNum);
    }
}