class DataTypeDefaults {

    static byte x;
    static short y;
    static int z;
    static long a;
    static float b;
    static double c;
    static char d;
    static boolean e;
    static String f;

    public static void main(String[] args) {

        System.out.println("Byte Default: " + x);
        System.out.println("Short Default: " + y);
        System.out.println("Int Default: " + z);
        System.out.println("Long Default: " + a);
        System.out.println("Float Default: " + b);
        System.out.println("Double Default: " + c);
        System.out.println("Char Default: " + d);
        System.out.println("Boolean Default: " + e);
        System.out.println("String Default: " + f);
    }
}